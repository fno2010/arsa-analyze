#!/usr/bin/env python

# Color print in terminal
black  = lambda s: "\033[0;30m" + s + "\033[0m"
red    = lambda s: "\033[0;31m" + s + "\033[0m"
green  = lambda s: "\033[0;32m" + s + "\033[0m"
blue   = lambda s: "\033[0;34m" + s + "\033[0m"
purple = lambda s: "\033[0;35m" + s + "\033[0m"
cyan   = lambda s: "\033[0;36m" + s + "\033[0m"
yellow = lambda s: "\033[1;33m" + s + "\033[0m"
white  = lambda s: "\033[1;37m" + s + "\033[0m"
